package com.huateng.ebank.entity.data.mng.base;

import java.util.Date;

/**
 * AbstractDepartmentInfo entity provides the base persistence definition of the
 * DepartmentInfo entity.
 *
 * @author MyEclipse Persistence Tools
 */

public abstract class AbstractDepartmentInfo implements java.io.Serializable {

	// Fields

	private long departmentCode;
	private String departmentName;
	private String brcode;
	private String status;
	private String lastUpdTlr;
	private Date lastUpdDate;
	private String BZ;
	private String misc;
	private String misc2;
	private String misc3;
	private String misc4;

	// Constructors



	/** default constructor */
	public AbstractDepartmentInfo() {
	}

	/** minimal constructor */
	public AbstractDepartmentInfo(long departmentCode, String departmentName, String brcode, String status) {
		this.departmentCode = departmentCode;
		this.departmentName = departmentName;
		this.brcode = brcode;
		this.status = status;
	}


	/** full constructor */

	public AbstractDepartmentInfo(long departmentCode, String departmentName,
			String brcode, String status, String lastUpdTlr, Date lastUpdDate,
			String BZ, String misc, String misc2, String misc3, String misc4) {
		super();
		this.departmentCode = departmentCode;
		this.departmentName = departmentName;
		this.brcode = brcode;
		this.status = status;
		this.lastUpdTlr = lastUpdTlr;
		this.lastUpdDate = lastUpdDate;
		this.BZ = BZ;
		this.misc = misc;
		this.misc2 = misc2;
		this.misc3 = misc3;
		this.misc4 = misc4;
	}


	// Property accessors

	public long getDepartmentCode() {
		return departmentCode;
	}

	public void setDepartmentCode(long departmentCode) {
		this.departmentCode = departmentCode;
	}

	public String getDepartmentName() {
		return departmentName;
	}

	public void setDepartmentName(String departmentName) {
		this.departmentName = departmentName;
	}

	public String getBrcode() {
		return brcode;
	}

	public void setBrcode(String brcode) {
		this.brcode = brcode;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getLastUpdTlr() {
		return lastUpdTlr;
	}

	public void setLastUpdTlr(String lastUpdTlr) {
		this.lastUpdTlr = lastUpdTlr;
	}

	public Date getLastUpdDate() {
		return lastUpdDate;
	}

	public void setLastUpdDate(Date lastUpdDate) {
		this.lastUpdDate = lastUpdDate;
	}

	public String getBZ() {
		return BZ;
	}

	public void setBZ(String bz) {
		BZ = bz;
	}

	public String getMisc() {
		return misc;
	}

	public void setMisc(String misc) {
		this.misc = misc;
	}

	public String getMisc2() {
		return misc2;
	}

	public void setMisc2(String misc2) {
		this.misc2 = misc2;
	}

	public String getMisc3() {
		return misc3;
	}

	public void setMisc3(String misc3) {
		this.misc3 = misc3;
	}

	public String getMisc4() {
		return misc4;
	}

	public void setMisc4(String misc4) {
		this.misc4 = misc4;
	}

}